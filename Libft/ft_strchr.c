#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	i;

	i = 0;
	while ((s && s[i]) && (char)c != s[i])
		i++;
	if (c == s[i])
		return ((char *)s + i);
	s++;
	return (NULL);
}
